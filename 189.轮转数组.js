// @lc code=start
/**
 * @param {number[]} nums
 * @param {number} k
 * @return {void} Do not return anything, modify nums in-place instead.
 */
var rotate = function(nums, k) {
    k = k % nums.length;
    const needRotateSubArr = nums.splice(nums.length - k, k);
    return nums.unshift(...needRotateSubArr);
};
